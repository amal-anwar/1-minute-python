txt = "Amal is \"not\" here" # \ stops an error from taking place in this sentence
print(txt)

print("This is\n a new line") #prints everything after \n in a new line

print(r'Hi\xHello')#r is a raw string that stops an error for \x

print("Hello\tamal")#will print hello amal

print("Hello\bamal")#will print hellamal

print("Hello\r amal")#will print amal
